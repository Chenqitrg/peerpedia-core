r"""Centralized message registry — public infrastructure, zero dependencies.

Every user-facing message has a **code** (machine-readable identifier),
**text** (template with ``{param}`` slots), **kind** (SUCCESS/ERROR/…),
and optional **suggestion** + **see_also** links.

All layers reference codes from this module — guards pass ``code=…`` to
exceptions, ``@with_context`` renders via ``cli/output.py``, and the REPL
displays the same messages.
"""

from __future__ import annotations

from dataclasses import dataclass
from enum import Enum, auto


class Kind(Enum):
    SUCCESS = auto()
    ERROR = auto()
    NOTIFY = auto()
    WARNING = auto()
    INFO = auto()


@dataclass(frozen=True)
class Msg:
    """One entry in the message catalog."""
    text: str
    kind: Kind = Kind.SUCCESS
    suggestion: str = ""
    see_also: tuple[str, ...] = ()
    log_text: str = ""


